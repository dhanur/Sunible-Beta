  <li><a><b>What is the benefit of Ownership?</b></a>
  <div>With ownership, you benefit from a lot more savings. This is because you get a 30% federal tax credit (and possibly other state, county and city incentives). Solar ownership also increases your property value. The system pays for itself in a few years or longer, depending on how big it is and how you finance it. Because of the low cost of solar and generous incentives, the payback is much quicker than it used to be.
  <br><br>If you can afford the upfront cost, Ownership is always the better choice.
  </div></li></ul></td></tr>
 
