  <li><a><b>How do I pay for solar?</b></a>
  <div>There are two main ways, Ownership and Solar-as-a-service.
  <br><br>Ownership is like buying a car, but with free gas, forever. And just like you would with a car, you can buy your solar system with cash upfront or through a low-cost solar loan. Solar-as-a-service is like a phone plan. It allows you to go solar with little to no upfront cost, and delivers savings from day 1.
  </div>
  </li></ul></td></tr>
 
