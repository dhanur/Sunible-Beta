  <li><a><b>What is the benefit of Solar-as-a-service?</b></a>
  <div>With Solar-as-a-service, you can go solar with little or no upfront cost and start saving money from day one. The solar panels will still be on your roof, but are owned and maintained (for free) by someone else.
  <br><br>Depending on which city you live in, you will either pay a monthly fee (that is lower than your current power bill) or for just the power you use (which will also be lower than your current power prices). Over 50% of solar homes in the U.S. chose Solar-as-a-service, presumably because it is quick, convenient and offers immediate savings.
  </div></li></ul></td></tr>
  
